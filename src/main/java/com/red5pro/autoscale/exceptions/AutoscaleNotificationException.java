package com.red5pro.autoscale.exceptions;

public class AutoscaleNotificationException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6975021978187338981L;

	public AutoscaleNotificationException() {
		super();
	}

	public AutoscaleNotificationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public AutoscaleNotificationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AutoscaleNotificationException(String arg0) {
		super(arg0);
	}

	public AutoscaleNotificationException(Throwable arg0) {
		super(arg0);
	}

}
